package dev.wilsondaniels.mydishes.domain.validation.handler;

import dev.wilsondaniels.mydishes.domain.exception.DomainException;
import dev.wilsondaniels.mydishes.domain.validation.ValidationHandler;
import dev.wilsondaniels.mydishes.domain.validation.Error;

import java.util.List;

public class ThrowsValidationHandler implements ValidationHandler {

    @Override
    public ValidationHandler append(final Error anError) {
        throw DomainException.with(anError);
    }

    @Override
    public ValidationHandler append(final ValidationHandler anHandler) {
        throw DomainException.with(anHandler.getErrors());
    }

    @Override
    public ValidationHandler validate(final Validation aValidation) {
        try {
            aValidation.validate();
        } catch (final Exception ex) {
            throw DomainException.with(new Error(ex.getMessage()));
        }

        return this;
    }

    @Override
    public List<Error> getErrors() {
        return List.of();
    }
}
